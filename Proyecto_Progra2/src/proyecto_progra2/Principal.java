                        mi_eliminarArbol.show(false);
                        mi_eliminarAtributo.show(false);
                        //mi_datosAtributo.show(false);
                        mi_eliminarMetodo.show(true);
                        //mi_descripcionMetodo.show(true);
                        mi_agregarMetodo.show(false);
